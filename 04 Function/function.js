// Function - block of code that performs a specific task.
// The Function object provides methods for functions. In JavaScript, every function is actually a Function object.

// Basic Function
function sayMyName() {
  console.log("Deep Chandra Joshi");
}
sayMyName();

// Function with perameter
function sayHello(name) {
  console.log(`Hello ${name}!`);
}
sayHello("Deep Chandra Joshi"); // argument

//   ====================================================================================================  //

// Add two number
function addTwoNumber(number1, number2) {
  // perameter
  console.log(number1 + number2);
}
addTwoNumber(4, 5); // Argument

//User Login
function loginUserMessage(userName) {
  return `${userName} Just Logged in`;
}
loginUserMessage("Deep Chandra Joshi");

