// Array.from
const arrayLike = {
  0: "a",
  1: "b",
  2: "c",
  length: 3,
};

const arr = Array.from(arrayLike);
console.log(arr); // Output: ['a', 'b', 'c']

// Converting a string to an array
const str = "hello";
const strArray = Array.from(str);
console.log(strArray); // Output: ['h', 'e', 'l', 'l', 'o']

